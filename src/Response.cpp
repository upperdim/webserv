#include "Response.hpp"

Response::Response()
{
}

Response::Response(const Response& other)
{
	data = other.data;
}

Response::~Response()
{
}


/* ************************************************************************** */
/* ************************************************************************** */

Response&	Response::operator=(const Response& rhs)
{
	if (this != &rhs)
	{
		data = rhs.data;
	}
	return (*this);
}


